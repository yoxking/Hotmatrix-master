package com.benet.collect.vmodel;

public class QuestRuleVo {
    private String questType;
    private int questLimit;
    private int questScore;

    public String getQuestType() {
        return questType;
    }

    public void setQuestType(String questType) {
        this.questType = questType;
    }

    public int getQuestLimit() {
        return questLimit;
    }

    public void setQuestLimit(int questLimit) {
        this.questLimit = questLimit;
    }

    public int getQuestScore() {
        return questScore;
    }

    public void setQuestScore(int questScore) {
        this.questScore = questScore;
    }
}
