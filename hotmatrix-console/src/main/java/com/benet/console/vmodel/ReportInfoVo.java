package com.benet.console.vmodel;

public class ReportInfoVo {

    private String reptNo;
    private String reptName;
    private String reptImage;
    private String reptType;
    private String reptDesc;
    private String checkState;
    private String pubDate;

    public String getReptNo() {
        return reptNo;
    }

    public void setReptNo(String reptNo) {
        this.reptNo = reptNo;
    }

    public String getReptName() {
        return reptName;
    }

    public void setReptName(String reptName) {
        this.reptName = reptName;
    }

    public String getReptImage() {
        return reptImage;
    }

    public void setReptImage(String reptImage) {
        this.reptImage = reptImage;
    }

    public String getReptType() {
        return reptType;
    }

    public void setReptType(String reptType) {
        this.reptType = reptType;
    }

    public String getReptDesc() {
        return reptDesc;
    }

    public void setReptDesc(String reptDesc) {
        this.reptDesc = reptDesc;
    }

    public String getCheckState() {
        return checkState;
    }

    public void setCheckState(String checkState) {
        this.checkState = checkState;
    }

    public String getPubDate() {
        return pubDate;
    }

    public void setPubDate(String pubDate) {
        this.pubDate = pubDate;
    }
}
